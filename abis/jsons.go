var (
	StandardERC20ABI   abi.ABI
	StandardERC721ABI  abi.ABI
	StandardERC1155ABI abi.ABI
)

func init() {
	parseAbi := func(name string, value string) abi.ABI {
		a, err := abi.JSON(bytes.NewReader([]byte(value)))
		if err != nil {
			panic(fmt.Errorf("parse %s ABI failed: %w", name, err))
		}
		return a
	}
	StandardERC20ABI = parseAbi("ERC20", erc20)
	StandardERC721ABI = parseAbi("ERC721", erc721)
	StandardERC1155ABI = parseAbi("ERC1155", erc1155)
}
